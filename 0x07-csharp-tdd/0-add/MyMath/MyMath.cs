using System;

namespace MyMath
{
    /// <summary>Operations Class</summary>
    public class Operations
    {
            /// <summary>
            /// Operations is for addition of 2 ints
            /// <param name="a">Is one of 2 ints</param>
            /// <param name="b">Is the second of 2 ints</param>
            ///</summary>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
